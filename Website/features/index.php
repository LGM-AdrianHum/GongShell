        <h1>Features</h1>
        <h2>Controls</h2>
        <p>The Gong Solutions Shell library provides a wide range of
        controls:</p>
        
        <table id="features">
            <tr><td><img src="images/shellview.png" alt="ShellView screenshot"/></td>
            <td>The <span class="component">ShellView</span> control displays the
            contents of a folder in the same manner as the right-hand pane in
            Windows Explorer, or a file dialog. It has properties for customising
            the appearance of the items in the control, and for showing the
            WebView pane in Windows XP and higher.</td></tr>
            
            <tr><td><img src="images/shelltreeview.png" alt="ShellTreeView screenshot"/></td>
            <td>The <span class="component">ShellTreeView</span> control displays the
            hierachy of the Windows Shell Namespace in the same manner as the left-
            hand pane in Windows Explorer. In addition, the root folder can be set
            to a folder other than the Desktop.</td></tr>

            <tr><td><img src="images/shellcombobox.png" alt="ShellComboBox screenshot"/></td>
            <td>The <span class="component">ShellComboBox</span> control displays the
            hierachy of the Windows Shell Namespace in a drop-down list in the same 
            manner as seen in a file dialog. In addition, the root folder can be set
            to a folder other than the Desktop, and the folders shown can be filtered.
            </td></tr>

            <tr><td><img src="images/filedialogtoolbar.png" alt="FileDialogToolbar screenshot"/></td>
            <td>The <span class="component">FileDialogToolbar</span> control displays 
            a toolbar the same as seen in a file dialog. It can automatically navigate
            a <span class="component">ShellView</span> in response to the user's
            input.
            </td></tr>

            <tr><td><img src="images/filenamecombobox.png" alt="FileNameComboBox screenshot"/></td>
            <td>The <span class="component">FileNameComboBox</span> control provides
            a filename text box as seen in a file dialog. It can automatically navigate
            a <span class="component">ShellView</span> in response to the user's
            input and provides full auto-completion of file and folder names.
            </td></tr>

            <tr><td><img src="images/filefiltercombobox.png" alt="FileFilterComboBox screenshot"/></td>
            <td>The <span class="component">FileFilterComboBox</span> control provides
            a file filter list as seen in a file dialog. It can automatically filter
            the items in a <span class="component">ShellView</span> in response to the 
            user's input.
            </td></tr>

            <tr><td><img src="images/placestoolbar.png" alt="PlacesToolbar screenshot"/></td>
            <td>The <span class="component">PlacesToolbar</span> control provides
            a "Places" toolbar as seen in a file dialog. It can automatically navigate
            a <span class="component">ShellView</span> in response to the user's input.
            </td></tr>
        </table>
