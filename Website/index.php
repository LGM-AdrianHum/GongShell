        <h1>Gong Solutions Shell Library</h1>

        <p>The Gong Solutions Shell Library provides controls to enable 
        embedding Windows Explorer-like functionality into .Net 2.0 
        programs and for creating custom file dialogs. It provides a clean 
        interface into the Windows Shell Namespace from code, allowing
        easy access to virtual folders such as the Recycle Bin.</p>
        
        <center><img src="images/filedialog-screen.png" alt="Screenshot" /></center>
        
        <h1>Easily Create Cusom File Dialogs</h1>
        
        <p>Using the Gong Solutions Shell Library, you can create custom
        file dialogs with just a few lines of code. Just drop a 
        <span class="component">FileDialogToolbar</span> control and a 
        <span class="component">ShellView</span> control onto your form,
        link them together and you have a basic file dialog!</p>
        
        <p><a href="tutorials/filedialog.php">See the tutorial here</a></p>

        <h1>Embed Windows Explorer in your Applications</h1>
        
        <p>Using the Gong Solutions Shell Library, you can embed Windows
        explorer in your applications by simply adding two controls. 
        Just drop a ShellTreeView control and a ShellView control onto 
        your form, link them together and you can be browsing in no time!</p>
        
        <p><a href="tutorials/explorer.php">See the tutorial here</a></p>

        <h1>Access the Windows Shell from Code</h1>
        
        <p>The Windows Shell is a notoriously difficult API to code to.
